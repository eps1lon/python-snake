  def testRandomApples(self):
    game = SnakeGame(None, 5, 5)

    unoccupied = game.screen().unoccupiedPoints()
    # random testing
    for _ in range(0, 500):
      apple = game.createRandomApple()

      self.assertIsNotNone(apple)
      self.assertIn(apple, unoccupied)
